// Swap a variable using function

public class Swap_a_var {
    public static void main(String[] args) {
        int a=69;
        int b=49;

        System.out.println("Value of a before swapping"+a);
        System.out.println("Value of a before swapping"+b);
         
        int temp;
        temp = a;
        a=b;
        b=temp;



        System.out.println("Value of a After swapping"+a);
        System.out.println("Value of a After swapping"+b);
    }

    // Functions Using call by referance

    // OOPS Java Dont support pass by refrance

    // Thus swaping is not possible using function
    
}
