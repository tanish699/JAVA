public class while_for_loop {
    public static void main(String[] args) {
        int n=0;
        int m=0;

        while(n<=20){
            if(n%2==0){
                System.out.print(n+ " ");
                
            }
            n++;
        }
         
        System.out.println( " ");
        for(int i=0; i<=20; i++){
            if(m%2==0){
                System.out.print(m+ " ");
                
            }
            m++;
        }
    }
    
}
